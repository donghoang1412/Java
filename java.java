public class java {
    public static void main(String[] args)
    {
        System.out.println("Hello World");
        System.out.println(returnSum(10,20));
        System.out.println(returnDifferent(40,36));
    }
    public int returnSum (int a, int b)
    {
        return a+b;
    }
    public int returnDifferent (int x, int y)
    {
        return x-y;
    }
}